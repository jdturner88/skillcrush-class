
weather = 'hurricane'

case weather
when 'sunny'
	puts "Hooray! Head to the beach!"
when 'raining'
	puts "Don't forget an umbrella, ella ella ay ay."
when 'snowing'
	puts "Better bundle up to stay warm. But snowball fights are so fun!"
when 'hail'
	puts "Oh man, better stay inside to stay safe!"
when 'foggy'
	puts "Be careful when driving! It's hard to see."
else
	puts "Batten down the hatches! It sounds like you're in for a hurricane, tornado, or some other catastrophic event!"
end